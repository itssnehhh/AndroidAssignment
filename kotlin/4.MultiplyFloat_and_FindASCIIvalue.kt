//Kotlin Program to Multiply two Floating Point Numbers

fun main() {

    println("Multiply two Floating Point Numbers")

    println("Enter the num1 :- ")
    var num1 = readLine()!!.toFloat()

    println("Enter the num2 :- ")
    var num2 = readLine()!!.toFloat()

    println("Multiplication of $num1 and $num2 = ${num1 * num2}")

// Program to Find ASCII value of a character

    println("Find ASCII value of a character")

